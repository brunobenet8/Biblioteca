    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            if(jTextField1.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Campo ID vazio. Operação não processada!");
            }else{
            EntityManagerFactory h = Persistence.createEntityManagerFactory("BibliotecaPU");;
            AcervoJpaController ac = new AcervoJpaController(h);
            ac.destroy(Integer.parseInt(jTextField1.getText()));
            JOptionPane.showMessageDialog(rootPane, "Dados excluídos com sucesso!");
            limpacampos();
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            EntityManagerFactory h = Persistence.createEntityManagerFactory("BibliotecaPU");;
            AcervoJpaController ac = new AcervoJpaController(h);            
            jTextField1.setText(String.valueOf(ac.findAcervo(Integer.parseInt(jTextField1.getText())).getId()));
            jTextField2.setText(ac.findAcervo(Integer.parseInt(jTextField1.getText())).getTitulo());
            jTextField4.setText(ac.findAcervo(Integer.parseInt(jTextField1.getText())).getAutor());
            jTextField5.setText(ac.findAcervo(Integer.parseInt(jTextField1.getText())).getAssunto());
            jTextField6.setText(ac.findAcervo(Integer.parseInt(jTextField1.getText())).getAno());
            jTextField7.setText(ac.findAcervo(Integer.parseInt(jTextField1.getText())).getEditora());
        }catch(Exception e){
            System.out.println(""+e);
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrados!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

