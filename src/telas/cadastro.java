    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty()){
           try{
            Acervo a = new Acervo();
            a.setAutor(jTextField4.getText());
            a.setTitulo(jTextField2.getText());
            a.setAssunto(jTextField5.getText());
            a.setAno(jTextField6.getText());
            a.setEditora(jTextField7.getText());
            EntityManagerFactory b = Persistence.createEntityManagerFactory("BibliotecaPU");;
            AcervoJpaController aj = new AcervoJpaController(b);
            aj.create(a);
            JOptionPane.showMessageDialog(rootPane, "Dados Cadastrados com sucesso!");
            limpacampos();
           }catch(Exception e){
               System.out.println(""+e); 
           }
        }else{
            try{
                Acervo a = new Acervo();
                a.setId(Integer.parseInt(jTextField1.getText()));
                a.setAutor(jTextField4.getText());
                a.setTitulo(jTextField2.getText());
                a.setAssunto(jTextField5.getText());
                a.setAno(jTextField6.getText());
                a.setEditora(jTextField7.getText());
                EntityManagerFactory h = Persistence.createEntityManagerFactory("BibliotecaPU");;
                AcervoJpaController ac = new AcervoJpaController(h);
                ac.edit(a);                
                JOptionPane.showMessageDialog(rootPane, "Dados alterados com sucesso!");
                limpacampos();
            }catch(Exception j){
                System.out.println(""+j);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

